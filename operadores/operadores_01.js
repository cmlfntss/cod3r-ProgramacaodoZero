/*

OPERADORES BINÁRIOS:
-> São os operadores que operam em cima de dois operandos, por isso são os binários, como por exemplo:

3 + 4;

OPERADORES UNÁRIOS:
-> São os operadores que operam em cima de um único operando, como por exemplo:

a++

OPERADORES TERNÁRIOS:
-> São os operadores que operam em três partes, como por exemplo:

x? a : b;

*/